#include <iostream>
using namespace std;

int main(){
    /*
    < less than
    > greater than
    == equal to
    <= less than or equal to
    >= greater than or equal to
    != not equal to
    */

    cout << (5>3) << endl;
    cout << (5<3) << endl;

    cout << (4==8) << endl;
    cout << (8==8) << endl;

    cout << (4!=8) << endl;

    int x = 2, y = 7;

    bool a = (x==y);
    cout << a << endl;
}