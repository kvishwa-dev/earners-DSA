# include <iostream>
using namespace std;

int main(){
    cout << 5+2 << endl;
    cout << 5-2 << endl;
    cout << 5*2 << endl;

    cout << 5/2 << endl; // output - int
    cout << 5.0/2 << endl; // output - float
    cout << 5/2.0 << endl; // output - float
    cout << 5.0/2.0 << endl; // output - float
}